// Человеческий язык журнала (PLAN-PRODUCTION §1.5): превращаем внутренние технические токены в
// понятные обычному человеку фразы. Применяется при ОТОБРАЖЕНИИ (LogModal / ActivityBell) — БД
// хранит исходные строки как есть (внутренняя диагностика), а пользователь видит понятный текст.

// Диагностические строки «пустых циклов» — обычному пользователю не нужны (шум). По умолчанию
// скрыты; показываются только под переключателем «Подробно».
const DIAGNOSTIC_RX: RegExp[] = [
  /^Уведомления прочитаны:/i,
  /^Новых подписок нет/i,
  /^Заявок в подписчики нет/i,
  /все уже обработаны/i,
  /Первый проход \(базлайн\)/i,
  /браузерная сессия занята/i,
  /детект отложен/i,
]
export function isDiagnostic(msg: string): boolean {
  return DIAGNOSTIC_RX.some((r) => r.test(msg))
}

// Замены жаргона → человеческий текст. Порядок важен: более длинные/специфичные — раньше.
const REPLACEMENTS: [RegExp, string][] = [
  [/Body is unusable:\s*Body has already been read/gi, 'временный сбой чтения — повторим автоматически'],
  [/Body has already been read/gi, 'временный сбой чтения — повторим автоматически'],
  [/login_required:\s*сессия недействительна\s*—?\s*нужен повторный вход/gi, 'сессия аккаунта истекла — войдите заново'],
  [/\blogin_required\b/gi, 'сессия истекла'],
  [/Таймаут\s*\d+\s*с:\s*браузерный воркер не ответил\s*\([^)]*\)/gi, 'аккаунт отвечал слишком долго — повторим автоматически'],
  [/браузерный воркер не ответил/gi, 'аккаунт отвечал слишком долго'],
  [/\(\/session\/run\)/gi, ''],
  [/\/session\/run/gi, ''],
  [/\(news\/inbox\)/gi, ''],
  [/news\/inbox/gi, 'уведомления'],
  [/\(self-events\)\s*/gi, ''],
  [/\bself-events\b/gi, 'уведомления'],
  [/\bbrowserState\b/gi, 'сессия'],
  [/\bstorageState\b/gi, 'сессия'],
  [/\bcheckpoint\b/gi, 'подтверждение входа'],
  [/\bchallenge\b/gi, 'подтверждение входа'],
  [/выполняю действия по (\d+)/gi, 'обрабатываю $1'],
  [/dm_closed[:\s][^,;)]*/gi, 'личка закрыта'],
  [/not_delivered[:\s][^,;)]*/gi, 'сообщение не доставлено'],
  [/\bunconfirmed[:\s][^,;)]*/gi, 'доставка не подтверждена'],
  [/no_posts[:\s][^,;)]*/gi, 'нет постов'],
  [/no_stories[:\s][^,;)]*/gi, 'нет активных сторис'],
  // Токены действий с человеческим пояснением следом (token: пояснение) — заменяем всё вместе,
  // чтобы не было «follow_button_not_found: кнопка не найдена» с дублем.
  [/follow_button_not_found[:\s][^,;)]*/gi, 'кнопка «Подписаться» не найдена'],
  [/comment_input_not_found[:\s][^,;)]*/gi, 'поле комментария не найдено'],
  [/captcha_input_not_found[:\s][^,;)]*/gi, 'поле капчи не найдено'],
  // Голые токены-исходы (появляются без пояснения)
  [/\bsession_rejected\b/gi, 'сессия отклонена Instagram — войдите заново'],
  [/\bwrong_profile\b/gi, 'профиль недоступен (удалён/переименован)'],
  [/\bproxy_dead\b/gi, 'прокси не отвечает'],
  [/\bapproval_pending\b/gi, 'ожидается подтверждение входа в приложении'],
  [/\bbad_password\b/gi, 'вход не принят'],
  [/\bfetchFailed\b/gi, 'не удалось прочитать'],
  // Префиксы-метки в сводных сообщениях действий
  [/(^|[\s;(])dm:\s*/gi, '$1директ: '],
  [/\bnetwork:\s*/gi, ''],
  [/\bPAUSED\b/g, 'на паузе'],
  [/\bCHALLENGE\b/g, 'требуется вход'],
  [/\bBLOCKED\b/g, 'заблокирован'],
]

// Превратить строку журнала в человеческую. Идемпотентна и безопасна для любых входов.
export function humanizeLog(msg: string): string {
  if (!msg) return msg
  let s = msg
  for (const [rx, rep] of REPLACEMENTS) s = s.replace(rx, rep)
  // подчистка после замен: пустые скобки, двойные пробелы, пробел перед пунктуацией
  s = s.replace(/\(\s*\)/g, '').replace(/\s{2,}/g, ' ').replace(/\s+([.,;:])/g, '$1').trim()
  return s
}
